package beta;

public class MDTO {
	
	private String ID;
	private String PW;
	private String NICK;
	private int POINT;
	private int protect;
	
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String pW) {
		PW = pW;
	}
	public String getNICK() {
		return NICK;
	}
	public void setNICK(String nICK) {
		NICK = nICK;
	}
	public int getPOINT() {
		return POINT;
	}
	public void setPOINT(int getPOINT) {
		POINT = getPOINT;
	}
	public int getProtect() {
		return protect;
	}
	public void setProtect(int protect) {
		this.protect = protect;
	}

	
	

}
